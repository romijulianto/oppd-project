                        doc.ExportCaption(Division);
                        doc.ExportCaption(Area);
                        doc.ExportCaption(ETL_Date);
                    }
                    doc.EndExportRow();
                }
            }
            int recCnt = startRec - 1;
            // Read first record for View Page // DN
            if (exportType == "view") {
                await dataReader.ReadAsync();
            // Move to and read first record for list page // DN
            } else {
                if (Connection.SelectOffset) {
                    await dataReader.ReadAsync();
                } else {
                    for (int i = 0; i < startRec; i++) // Move to the start record and use do-while loop
                        await dataReader.ReadAsync();
                }
            }
            int rowcnt = 0; // DN
            do { // DN
                recCnt++;
                if (recCnt >= startRec) {
                    rowcnt = recCnt - startRec + 1;

                    // Page break
                    if (ExportPageBreakCount > 0) {
                        if (rowcnt > 1 && (rowcnt - 1) % ExportPageBreakCount == 0)
                            doc.ExportPageBreak();
                    }
                    LoadListRowValues(dataReader);

                    // Render row
                    RowType = RowType.View; // Render view
                    ResetAttributes();
                    await RenderListRow();
                    if (!doc.ExportCustom) {
                        doc.BeginExportRow(rowcnt); // Allow CSS styles if enabled
                        if (exportType == "view") {
                            await doc.ExportField(_UserName);
                            await doc.ExportField(UserPassword);
                            await doc.ExportField(_UserLevel);
                            await doc.ExportField(UserEmail);
                            await doc.ExportField(Full_Name);
                            await doc.ExportField(Position);
                            await doc.ExportField(Department);
                            await doc.ExportField(Division);
                            await doc.ExportField(Area);
                            await doc.ExportField(ETL_Date);
                        } else {
                            await doc.ExportField(_UserName);
                            await doc.ExportField(UserPassword);
                            await doc.ExportField(_UserLevel);
                            await doc.ExportField(UserEmail);
                            await doc.ExportField(Full_Name);
                            await doc.ExportField(Position);
                            await doc.ExportField(Department);
                            await doc.ExportField(Division);
                            await doc.ExportField(Area);
                            await doc.ExportField(ETL_Date);
                        }
                        doc.EndExportRow(rowcnt);
                    }
                }

                // Call Row Export server event
                if (doc.ExportCustom)
                    RowExport(doc, dataReader);
            } while (recCnt < stopRec && await dataReader.ReadAsync()); // DN
            if (!doc.ExportCustom)
                doc.ExportTableFooter();
        }

